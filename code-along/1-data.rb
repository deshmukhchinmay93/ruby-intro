# In Ruby, there are different *types* of data:

# Numbers
puts 5443 / 330.0
puts 4534545345*2342342352
# Non-numbers
puts "Hello World" + " How are you" * 5
puts "Hello:" + 5.to_s
puts "" ""
# True, False
puts true
puts false

# Nothing

puts nil

# Variables
x = 10
y = 6
puts x + y
food = "Samosa"
quantity = 3

puts food * quantity

# Combine strings and variables

first_name = "Pintu"
greeting = "Hello #{first_name}"
puts greeting

# String manipulation

puts "Hello".length
puts "how".capitalize
puts "Garden".reverse

text = "My name is Anthony"
puts text.uppercase
puts text.downcase
puts text.reverse